/**JavaScript pode alterar o conteúdo HTML 
 *
 * Um dos muitos métodos JavaScript HTML é getElementById().
 *
 * O exemplo abaixo "encontra" um elemento HTML (com id="demo") e altera o conteúdo 
 * do elemento (innerHTML) para "Hello JavaScript" 
*/

document.getElementById("demo").innerHTML = "Hello JavaScript";

// JavaScript pode alterar valores de atributos HTML

/**JavaScript pode alterar estilos HTML (CSS)
 * 
 * Alterar o estilo de um elemento HTML é uma variante de alterar um atributo
 * HTML.
*/

document.getElementById("demo").style.fontSize = "35px";

/**JavaScript pode ocultar elementos HTML
 * 
 * È possível ocultar elementos HTML alterando o display estilo.
*/

document.getElementById("demo").style.display = "none";

/**JavaScript pode mostrar elementos HTML
 * 
 * Mostrar elementos HTML ocultos também pode ser feito alterando o display.
*/

document.getElementById("demo").style.display = "block";

